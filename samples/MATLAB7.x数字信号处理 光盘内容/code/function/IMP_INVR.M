function [b,a] = imp_invr(c,d,T)
% 模拟到数字滤波器的变换的脉冲响应不变法
% ---------------------------------------------------------------
% [b,a] = imp_invr(c,d,T)
%  b = 数字滤波器的z^(-1)分子多项式
%  a = 数字滤波器的z^(-1)分母多项式
%  c = 模拟滤波器的s的分子多项式
%  d =  模拟滤波器的s的分母多项式
%  T = 采样(变换)参数
%
[R,p,k] = residue(c,d);
p = exp(p*T);
[b,a] = residuez(R,p,k);
b = real(b'); a = real(a');
