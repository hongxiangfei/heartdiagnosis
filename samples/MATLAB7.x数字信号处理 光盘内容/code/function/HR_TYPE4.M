function [Hr,w,d,L] = Hr_Type4(h);
% 计算第四种低通滤波器设计的振幅响应Hr(w) 
% ---------------------------------------------------
% [Hr,w,d,L] = Hr_Type4(h)
%  Hr = 振幅响应
%  w = 在[0 pi] 区间内计算Hr 的500个频率点
%  d = 第四种低通滤波器的系数
%  L = Hr的阶次
%  h = 第四种低通滤波器的频率响应
% 
 M = length(h);
 L = M/2;
 d = 2*[h(L:-1:1)];
 n = [1:1:L]; n = n-0.5;
 w = [0:1:500]'*pi/500;
Hr = sin(w*n)*d';

