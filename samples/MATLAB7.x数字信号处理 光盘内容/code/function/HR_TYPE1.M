function [Hr,w,a,L] = Hr_Type1(h);
% 计算第一种低通滤波器设计的振幅响应Hr(w) 
% -----------------------------------------------------------
% [Hr,w,a,L] = Hr_Type1(h)
%  Hr = 振幅响应
%  w = 在[0 pi] 区间内计算Hr 的500个频率点
%  a = 第一种低通滤波器的系数
%  L = Hr的阶次
%  h = 第一种低通滤波器的频率响应
%
 M = length(h);
 L = (M-1)/2;
 a = [h(L+1) 2*h(L:-1:1)]; % 1乘(L+1) 行向量row vector
 n = [0:1:L];              % (L+1)乘1 列向量
 w = [0:1:500]'*pi/500;
Hr = cos(w*n)*a';
