function [Hr,w,c,L] = Hr_Type3(h);
% 计算第三种低通滤波器设计的振幅响应Hr(w) 
% -----------------------------------------------------------
% [Hr,w,c,L] = Hr_Type3(h)
%  Hr = 振幅响应
%  w = 在[0 pi] 区间内计算Hr 的频率点
%  c = 第三种低通滤波器的系数
%  L = Hr的阶次
%  h = 第三种低通滤波器的频率响应
%
 M = length(h);
 L = (M-1)/2;
 c = [2*h(L+1:-1:1)];
 n = [0:1:L];
 w = [0:1:500]'*pi/500;
Hr = sin(w*n)*c';
